package de.incunabulum.jakuzi.exceptions;

public class JakuziException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1903386932364770194L;

	public JakuziException() {
		super();
	}

	public JakuziException(String arg0) {
		super(arg0);
	}

	public JakuziException(Throwable arg0) {
		super(arg0);
	}

	public JakuziException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
