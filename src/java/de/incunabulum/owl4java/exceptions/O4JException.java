package de.incunabulum.owl4java.exceptions;

public class O4JException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1903386932364770194L;

	public O4JException() {
		super();
	}

	public O4JException(String arg0) {
		super(arg0);
	}

	public O4JException(Throwable arg0) {
		super(arg0);
	}

	public O4JException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
